#include <stdio.h>

int main(){
    //arithmatic operation
    // +, - , *, %, ++, --
    
    //relational operation
    // ==, !=, >, <, >=, <=
    int a=1;
    int b=5;
    printf("a==b : %d\n", a==b );
    printf("a!=b : %d\n", a!=b );
    printf("a>b : %d\n", a>b );
    printf("a<b : %d\n", a<b );
    printf("a>=b : %d\n", a>=b );
    printf("a<=b : %d\n", a<=b );
    
    //bit-wise operation
    // &, |, ^, ~, <<, >>
    
    //logical operation
    // &&(AND), ||(OR), !(NOT)
    int man=1;
    int age=1;
    int inkorea=1;
    
    printf("%d", man && age && inkorea);
    
    //assignment(배정) operation
    // =, +=, -=, *=, /=, %=
    
    return 0;
}
